function toTitleCase(str) {
    return str.replace(
      /\w\S*/g, function(txt){return txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase();}
    ).replace(
      /\s([a-zA-Z]{1,2})\s/g, function(s) { return s.toLowerCase()} // De > de, O > o, etc
    );
}


